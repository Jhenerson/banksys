    private void btnSacarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacarActionPerformed
        try {
            float valor = Float.parseFloat(txtValor.getText());
            if(valor > 0) {
                NConta nc = new NConta();
                Conta c = nc.consultarID(id_conta);
                c.sacar(valor);
                nc.salvar(c);

                NCliente ncli = new NCliente();
                Cliente cli = ncli.consultarID(id_cliente);

                NMovimentacao nm = new NMovimentacao();
                Movimentacao mov = new Movimentacao();
                mov.setCliente(cli);
                mov.setConta(c);
                mov.setTipoMovimentacao(1);
                mov.setValor(valor);

                Date dataAtual = new Date();
                java.sql.Date data_mov = new java.sql.Date(dataAtual.getTime());
               
                mov.setData_hora(data_mov);

                nm.salvar(mov);

                JOptionPane.showMessageDialog(rootPane, "Saque no valor de R$" + valor + " efetuado com sucesso!");
                this.dispose();
                
            } else {
                JOptionPane.showMessageDialog(rootPane, "Valor inv√°lido. Tente novamente");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }//GEN-LAST:event_btnSacarActionPerformed

